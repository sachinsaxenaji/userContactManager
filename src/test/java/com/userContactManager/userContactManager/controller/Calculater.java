package com.userContactManager.userContactManager.controller;

public class Calculater {

    public int sum(int a, int b, int c)
    {
        return a+b+c;
    }
    public int mul(int a, int b)
    {
        return a*b;
    }

    public boolean comp(int a, int b)
    {
        return a==b;
    }
}
